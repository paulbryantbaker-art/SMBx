/**
 * Reads SMBX_SAMPLE_DEALS_500_PAUL_BAKER.xlsx and emits
 * server/migrations/057_seed_pbaker_sample_deals.sql.
 *
 *   node scripts/generate-pbaker-seed-migration.mjs
 *
 * The generated migration:
 *   1. Upserts pbaker@smbx.ai with is_advisor=true.
 *   2. Inserts 500 deals owned by that user.
 *   3. Idempotency: each row has financials.sample_id (e.g. "DEAL-0001");
 *      re-running the migration is a no-op once the rows are present.
 */
import XLSX from "xlsx";
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const XLSX_PATH = resolve(ROOT, "SMBX_SAMPLE_DEALS_500_PAUL_BAKER.xlsx");
const OUT_PATH = resolve(ROOT, "server/migrations/057_seed_pbaker_sample_deals.sql");

/* ─── Mappings ───────────────────────────────────────────────── */

// Excel "Deal Type" → DB journey_type. Codebase only knows sell/buy/raise/pmi.
// Divest collapses into sell, Merge into buy. Original label preserved in exit_type.
const JOURNEY_FROM_DEAL_TYPE = {
  Sell:   "sell",
  Divest: "sell",
  Buy:    "buy",
  Merge:  "buy",
  Raise:  "raise",
};

// Excel "Status" → (gate_index, deal_status).
//   gate_index → S{n}/B{n}/R{n} based on journey
//   deal_status applies to deals.status (default 'active')
const STATUS_RULES = {
  "Prospect":             { gate: 0, status: "active"  },
  "Initial Conversation": { gate: 0, status: "active"  },
  "Due Diligence":        { gate: 3, status: "active"  },
  "LOI Stage":            { gate: 4, status: "active"  },
  "Closing Soon":         { gate: 5, status: "active"  },
  "Closed":               { gate: 5, status: "closed"  },
  "Stalled":              { gate: null, status: "stalled" },
};

const GATE_PREFIX = { sell: "S", buy: "B", raise: "R", pmi: "PMI" };

/* ─── Helpers ───────────────────────────────────────────────── */

function dollarsStringToCents(s) {
  if (s == null) return null;
  const n = Number(String(s).replace(/[^0-9.\-]/g, ""));
  if (!Number.isFinite(n)) return null;
  return Math.round(n * 100);
}

function multipleStringToNumber(s) {
  if (s == null) return null;
  const n = Number(String(s).replace(/[^0-9.\-]/g, ""));
  return Number.isFinite(n) ? n : null;
}

function deriveGate(journey, statusLabel) {
  const rule = STATUS_RULES[statusLabel];
  if (!rule || rule.gate == null) return null;
  const prefix = GATE_PREFIX[journey] ?? "S";
  return `${prefix}${rule.gate}`;
}

function deriveStatus(statusLabel) {
  return STATUS_RULES[statusLabel]?.status ?? "active";
}

/* ─── Load + transform ──────────────────────────────────────── */

const wb = XLSX.readFile(XLSX_PATH);
const rows = XLSX.utils.sheet_to_json(wb.Sheets["Deals"], { defval: null });

if (rows.length !== 500) {
  console.warn(`Expected 500 rows, got ${rows.length}. Continuing.`);
}

const records = rows.map((r) => {
  const dealType = r["Deal Type"];
  const journey = JOURNEY_FROM_DEAL_TYPE[dealType] ?? "sell";
  const statusLabel = r["Status"];
  const gate = deriveGate(journey, statusLabel);
  const status = deriveStatus(statusLabel);

  return {
    sample_id: r["Deal ID"],
    business_name: r["Company Name"],
    location: r["Location"],
    industry: r["Industry"],
    journey_type: journey,
    current_gate: gate,
    league: r["League"],
    sde_cents: dollarsStringToCents(r["EBITDA/SDE ($)"]),
    revenue_cents: dollarsStringToCents(r["Revenue ($)"]),
    asking_cents: dollarsStringToCents(r["Enterprise Value ($)"]),
    multiple: multipleStringToNumber(r["EV/EBITDA Multiple"]),
    deal_status: status,
    exit_type: r["Transaction Type"],
    deal_type_label: dealType,
    status_label: statusLabel,
    notes: r["Notes"],
    est_close: r["Est. Close Date"],
    created_at: r["Created Date"],
  };
});

// Sanity log
const tally = (k) => records.reduce((m, r) => (m[r[k]] = (m[r[k]] || 0) + 1, m), {});
console.log("journey_type:", tally("journey_type"));
console.log("current_gate:", tally("current_gate"));
console.log("deal_status:",  tally("deal_status"));

/* ─── Emit SQL ──────────────────────────────────────────────── */

const payload = JSON.stringify(records).replace(/'/g, "''"); // SQL-escape single quotes

const sql = `-- 057_seed_pbaker_sample_deals.sql
-- Seeds 500 sample deals for pbaker@smbx.ai (Broker/Advisor test account).
-- Idempotent: re-runs are no-ops because each row is keyed by
-- financials->>'sample_id' (e.g. "DEAL-0001").
--
-- Generated by scripts/generate-pbaker-seed-migration.mjs from
-- SMBX_SAMPLE_DEALS_500_PAUL_BAKER.xlsx.

-- 1. Upsert the broker user.
INSERT INTO users (email, display_name, role, is_advisor, league)
VALUES ('pbaker@smbx.ai', 'Paul Baker', 'user', true, 'L4')
ON CONFLICT (email) DO UPDATE
  SET is_advisor   = true,
      display_name = COALESCE(NULLIF(users.display_name, ''), EXCLUDED.display_name),
      league       = COALESCE(users.league, EXCLUDED.league),
      updated_at   = NOW();

-- 2. Insert the 500 deals owned by that user.
WITH owner AS (
  SELECT id FROM users WHERE email = 'pbaker@smbx.ai'
),
incoming AS (
  SELECT *
  FROM jsonb_to_recordset(
    '${payload}'::jsonb
  ) AS x(
    sample_id        text,
    business_name    text,
    location         text,
    industry         text,
    journey_type     text,
    current_gate     text,
    league           text,
    sde_cents        bigint,
    revenue_cents    bigint,
    asking_cents     bigint,
    multiple         numeric,
    deal_status      text,
    exit_type        text,
    deal_type_label  text,
    status_label     text,
    notes            text,
    est_close        text,
    created_at       text
  )
)
INSERT INTO deals (
  user_id,
  journey_type,
  current_gate,
  league,
  industry,
  location,
  business_name,
  name,
  revenue,
  sde,
  ebitda,
  asking_price,
  exit_type,
  financials,
  status,
  created_at,
  updated_at
)
SELECT
  o.id,
  i.journey_type,
  i.current_gate,
  i.league,
  i.industry,
  i.location,
  i.business_name,
  i.business_name,
  i.revenue_cents,
  i.sde_cents,
  i.sde_cents,                 -- ebitda mirrors sde for this sample data
  i.asking_cents,
  i.exit_type,
  jsonb_build_object(
    'sample_id',       i.sample_id,
    'deal_type_label', i.deal_type_label,
    'status_label',    i.status_label,
    'multiple',        i.multiple,
    'notes',           i.notes,
    'est_close',       i.est_close,
    'seed_source',     'SMBX_SAMPLE_DEALS_500_PAUL_BAKER'
  ),
  i.deal_status,
  COALESCE(NULLIF(i.created_at, '')::timestamptz, NOW()),
  NOW()
FROM incoming i
CROSS JOIN owner o
WHERE NOT EXISTS (
  SELECT 1 FROM deals d
  WHERE d.user_id = o.id
    AND d.financials->>'sample_id' = i.sample_id
);
`;

writeFileSync(OUT_PATH, sql);
console.log(`\nWrote ${OUT_PATH}`);
console.log(`Size: ${(sql.length / 1024).toFixed(1)} KB · rows: ${records.length}`);
